package com.leyun.comment;
public class CommentDemo {
    /**
     * 这是一个main方法，是程序的入口
     * 必须有这个main方法，程序才能执行
     */
    public static void main(String[] args) {
        // 目标：认识三种注释的写法
        // 1. 单行注释：
        // 打印2行诗句
        System.out.println("我欲乘风归去，又恐琼楼玉宇");

        /*
        2. 多行注释：
        打印3行诗句，依次打印
        */
        System.out.println("西风瘦马，客输西风");
        System.out.println("西风瘦马，客输西风");
        System.out.println("西风瘦马，客输西风");
    }
}
